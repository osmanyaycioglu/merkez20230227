package com.merkez.training.spring.firstspring;

public class Phone {
    private String name;
    private String number;

    public String getName() {
        return name;
    }

    public void setName(String nameParam) {
        name = nameParam;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String numberParam) {
        number = numberParam;
    }
}
